package manager.ADV02.bean;

public class GroupAdsBean {
	private String id;
	
	private String name;
	
	private String urlImage;
	
	private String perAndroid;
	
	private String perWindow;
	
	private String perIos;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getUrlImage() {
		return urlImage;
	}

	public void setUrlImage(String urlImage) {
		this.urlImage = urlImage;
	}

	public String getPerAndroid() {
		return perAndroid;
	}

	public void setPerAndroid(String perAndroid) {
		this.perAndroid = perAndroid;
	}

	public String getPerWindow() {
		return perWindow;
	}

	public void setPerWindow(String perWindow) {
		this.perWindow = perWindow;
	}

	public String getPerIos() {
		return perIos;
	}

	public void setPerIos(String perIos) {
		this.perIos = perIos;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
